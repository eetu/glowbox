// Unknown characters render as a hollow box — a visible "missing glyph" signal beats a
// silent gap when content carries an unexpected character.
const FALLBACK_ART = `
#####
#...#
#...#
#...#
#...#
#...#
#####`;

// Compile art → 7 row bitmasks, bit 4 (of 5) = the leftmost column. Runs once at load;
// pure string work, so the module stays import-safe in node/SSR.
function compile(art: string): number[] {
	return art
		.trim()
		.split('\n')
		.map((row) => {
			let bits = 0;
			for (let x = 0; x < FONT_5X7.width; x++)
				if (row[x] === '#') bits |= 1 << (FONT_5X7.width - 1 - x);
			return bits;
		});
}

const GLYPHS = new Map<string, number[]>(
	Object.entries(RAW).map(([ch, art]) => [ch, compile(art)])
);
const FALLBACK = compile(FALLBACK_ART);

/** The 7 row bitmasks (top→bottom) for one character; in each row, bit 4 is the
 *  leftmost of the 5 columns. Unknown characters get a hollow-box fallback glyph. */
export function glyph5x7(ch: string): readonly number[] {
	return GLYPHS.get(ch) ?? FALLBACK;
}
