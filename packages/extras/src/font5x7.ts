// The bundled LED font: a hand-authored 5×7 dot-matrix face covering printable ASCII
// (32–126). Glyphs are authored as ASCII art ('#' = lit) — the readable single source
// of truth — and compiled once at load into 7-row bitmasks. Deterministic across OSes
// (unlike the system-font path), DOM-free, and sized for real LED grids: at scale 1 a
// character cell is 6×8 (5×7 ink + 1 gap each way).

